// Primitive Data type

//String
const name = "Karan Sharma"
console.log(`Name :- ${name}`);

//type of

console.log(typeof(name));


//Number
const age = 22
console.log(`Age :- ${age}`);

//type of

console.log(typeof(age));


//Boolean
const isAvailable = true;
console.log(`Boolean ;- ${isAvailable}`);

//typeof

console.log(typeof(isAvailable));



//Undefined

const x = undefined ;

console.log(`Undefine :- ${x} `);

//type of

console.log(typeof(x));



//Null

const value = null;
console.log(`Null :- ${value}`);

//type of

console.log(typeof(value));

//Object

const obj = {
    name : "Kgopal",
    roll_No : 24
}

console.log(obj);

//TypeOf

console.log(typeof(obj));

